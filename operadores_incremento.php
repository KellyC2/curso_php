<?php
/**
 * Incremento: ++
 *      Pre-incremeneto:   ++$variable
 *      Post-incremento:   $variable ++
 * Decremento: --
 *      Pre-decremento:   --$variable
 *      Post-decremento:  $variable --
 * 
 * 
 */

 $numero=10;
 echo ++$numero; 
 echo $numero++;
 echo $numero;

 $numero2=15;
 echo --$numero2;
 echo $numero2--;
 echo $numero2;

?>